package Operator;

public class bitwiseOperator {
    public static void main(String[] args) {
        int a=20;
        int b=35;
        System.out.println("Logical And : "+((a>b)&&(a<b)));
        System.out.println("Logical OR  : "+((a>b)||(a<b)));
        System.out.println("Logical NOT : "+(!(a>b)));

    }
}
