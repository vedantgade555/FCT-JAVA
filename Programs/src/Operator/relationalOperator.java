package Operator;

public class relationalOperator {
    public static void main(String[] args) {
        int a=20;
        int b=35;
        System.out.println("Is "+a +" > "+b+" : "+(a>b));
        System.out.println("Is "+a +" < "+b+" : "+(a<b));
        System.out.println("Is "+a +" >= "+b+" : "+(a>=b));
        System.out.println("Is "+a +" <= "+b+" : "+(a<=b));
        System.out.println("Is "+a +" == "+b+" : "+(a==b));

    }


}
