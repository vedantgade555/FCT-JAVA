package Operator_1;

public class logicalOperator {
    public static void main(String[] args) {
        int a=20;
        int b=35;
        System.out.println("Bitwize And : "+(a&b));
        System.out.println("Bitwize OR  : "+(a|b));
        System.out.println("Bitwize XOR : "+(a^b));
        System.out.println("Bitwize Complement : "+(~a));
    }
}
